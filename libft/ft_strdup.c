#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*dst;

	i = 0;
	len = 0;
	while (s[len] != '\0')
		len++;
	dst = (char*)malloc(sizeof(char) * len + 1);
	if (dst == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
